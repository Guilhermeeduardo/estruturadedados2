#denação Estável e Instável
#Estável: Merge Sort, Bucket Sort, Radix Sort.
#Mantêm a ordem relativa de elementos iguais.
#Exemplo: Ordenar [1a, 2, 1b] por valor dá [1a, 1b, 2].

#Instável: Quick Sort, Selection Sort, Shell Sort.
#Não garantem ordem relativa de elementos iguais.
#Exemplo: Ordenar [1a, 2, 1b] por valor pode resultar em [1b, 1a, 2].